const reader = {
  name: "Julia",
  lastName: "Pessoa",
  age: 21,
  favoriteBooks: [
    {
      title: "O Senhor dos Anéis - a Sociedade do Anel",
      author: "J. R. R. Tolkien",
      publisher: "Martins Fontes",
    },
  ],
};

// 1. access the name, lastname and title keys and display information in a console.log() in the following format: “julia pessoa’s favorite book is called ‘O Senhor dos Anéis - a Sociedade do Anel’.”
console.log(
  `${reader.name} ${reader.lastName}'s favorite book is called ${reader.favoriteBooks[0].title}`,
);

// 2. add a new favorite book in the favoritebooks key, which is an array of objects. assign this key an object that contains the following information:
// {
//   title: 'Harry Potter e o Prisioneiro de Azkaban',
//   author: 'JK Rowling',
//   publisher: 'Rocco',
// }
let newBook = {
  title: "Harry Potter e o Prisioneiro de Azkaban",
  author: "JK Rowling",
  publisher: "Rocco",
};

const addFavoritBook = (reader, book) => reader.favoriteBooks.push(book);

addFavoritBook(reader, newBook);

